			 <h2>
				Кримінальна відповідальність за водіння в нетверезому вигляді
			 </h2>
            <p>При водінні транспортного засобу, перебуваючи в стані алкогольного або наркотичного сп'яніння, багато автовласників замислюються про те, як можна мінімізувати всі наслідки неприємної ситуації і не бути притягнутим до кримінальної відповідальності, що залишить слід в біографії людини на довгі роки. У цьому випадку на допомогу приходить Автоадвокат з адвокатського об'єднання «Брати Федорова», розташованого в місті Київ.</p>
			 <h3 style="font-size: 20px;">
				 Штрафи, сума штрафів за водіння в нетверезому вигляді
			 </h3>
            <p>Згідно зі статтею 286 примітка 1 Кримінального кодексу України, якщо даний кримінальний проступок було вчинено вперше, то винуватець буде покараний штрафом в розмірі від сімнадцяти до тридцяти тисяч гривень з обов'язковим позбавленням прав на керування автомобілем на термін до трьох років.</p>
            <p>При здійсненні повторного порушення, передбаченого в статті 286 примітка один, водій, який знаходиться в нетверезому вигляді, або відмовився від проходження медичного огляду повинен виплатити штраф розміром від тридцяти чотирьох до п'ятдесяти тисяч гривень з позбавленням прав на керування автомобілем та іншими транспортними засобами на термін, який варіюється від двох до трьох років включно.</p>
            <p>Водій, якого визнали винним за статтею 286 примітка 1 Кримінального кодексу України, вважається судимим до закінчення терміну заборони керування транспортним засобом, що тягне за собою різні негативні наслідки, часто — звільнення і неможливість влаштуватися на нову роботу. Щоб уникнути небажаної судимості, слід звернутися в нашу компанію. У нас в штабі є висококваліфікований Юрист, який допоможе вирішити всі проблеми, пов'язані з водінням авто в нетверезому вигляді або відмову від проходження медичного огляду.</p>

            <p>Серед кримінальних діянь, які несуть за собою штраф за водіння в нетверезому вигляді і позбавлення права керування транспортним засобом, можна виділити наступні:</p>

            <ul>
                <li>відмова водія від медичного огляду на стан сп'яніння;</li>
                <li>керування автотранспортним засобом, перебуваючи в стані будь-якого виду сп'яніння, включаючи алкогольне, наркотичне. Також сюди відноситься водіння авто після прийому медичних препаратів, де побічною дією є зниження концентрації уваги, пригнічення реакції;</li>
                <li>допуск до керування автомобільним транспортним засобом людини, що знаходиться під впливом ліків або в стані будь-якого сп'яніння;</li>
                <li>вживання водієм автотранспортного засобу, після зупинки співробітником поліції до момента проходження медичного огляду, алкогольних напоїв, наркотичних речовин, а також медичних препаратів, виготовлених на їх основі (крім тих одиниць, офіційно зареєстрованих в списку ліків, згідно із законом знаходяться в автомобільній аптечці або призначених лікуючим лікарем при наявності відповідного документа на їх прийом);</li>
                <li>вживання алкогольних напоїв, наркотичних речовин після ДТП.</li>
            </ul>
